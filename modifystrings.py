a = "Hello, World!"
print(a.upper())   #output  HELLO, WORLD!

a = "Hello, World!"
print(a.lower())   #output hello, world!

a = " Hello, World! "
print(a.strip()) #output "Hello, World!"

a = "Hello, World!"
print(a.replace("H", "J")) #output  Jello, World!

a = "Hello, World!"
print(a.split(",")) #output ['Hello', ' World!']